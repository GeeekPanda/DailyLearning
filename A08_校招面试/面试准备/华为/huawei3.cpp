/*
    题目：
        A公司需要在项目中引入某开源工程，需要评估该开源工程中某模块的编译时间。
        当前一直该项目中每个模块的编译时间以及其依赖的模块列表，在拥有无限数量的并行任务的情况下，
        求某个指定模块的最短编译时间。
        若模块间存在循环依赖或者依赖的模块不存在，则无法完成编译，返回-1。
    输入描述：
        第一行输入为目标模块名，
        以后每行输入定义一个模块，包含模块的名字，编译时间，依赖模块列表，用逗号隔开，若依赖的模块不存在，
        则表示可以独立编译，例如：
        module2，10，module1
        module1，10

        模块名只包含字母和数字且至少包含一个字符
        模块数量不超过50000个
    输出描述：
        输出最短编译时间，若无法完成编译则输出-1。
    示例1：
        输入：
            module3
            module1，10
            module2，5
            module3，10，module1，module2
        输出：
            20
        说明：
            module1编译需要10毫秒，module2编译需要5毫秒，module编译依赖module1和module2，同时自身编译也需要10毫秒，所以
            总编译时间 = 10 + max(10, 5) = 20毫秒。
    示例2：
        输入：
            module2
            module2，10，module1
        输出：
            -1
        说明：
            module1没有定义，无法完成编译，所以输出-1。
    示例3：
        输入：
            module2
            module2，10，module1
            module1，10，module2
        输出：
            -1
        说明：
            module1与module2出现循环依赖，无法完成编译，所以输出-1。
*/